// Mengimpor pustaka 'dart:math' untuk mengakses fungsi matematika.
import 'dart:math';

// Fungsi utama yang akan dieksekusi saat program dijalankan.
void main(List<String> args) {
  // Mendeklarasikan variabel 'a' sebagai bilangan floating point (double).
  double a;
  
  // Menghitung akar kuadrat dari 25.0 dan menyimpannya dalam variabel 'a'.
  a = sqrt(25.0);
  
  // Mencetak nilai dari variabel 'a', yang merupakan akar kuadrat dari 25.0.
  print(a);
  
  // Menghitung nilai 'a' dipangkatkan dengan 4 dan mencetak hasilnya.
  print(pow(a, 4));
}
